                        <p>
                            <strong>Abstract</strong><br/>
                            On the 23rd of March 2020, the Dutch government announced the ‘intelligent lockdown’ in the
                            Netherlands. To stop the spread of Covid-19, citizens were advised to work from home.
                            Since work is one of the most important sources of physical activity, and commentators
                            have expressed concerns over the impact of these measures on fitness, which is essential
                            for humans as it reduces the risk of many health problems. It is possible however, that
                            in parts of the society, the imposed limitations of normal daily activities, combined with
                            an increase in flexibility and free time, may have led to compensatory behaviour, for instance
                            by an increase in visits to nature reserves. Natuurmonumenten, a Dutch nature conservation
                            association, reported an increase in the number of website visitors, downloads and memberships.
                            It is suggested that this is related to COVID-19 crisis, but little more information is
                            provided on the distributions and patterns of the increased interest in natural reserves.
                            We hypothesize that Instagram locations posts can be used to give an insight to visits into
                            nature
