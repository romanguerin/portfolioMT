                        <h3>Project Report</h3>
                        <p>
                            Imagine that when you’re walking on the street, you hear someone calling your name;
                            or when you’re alone at night, you hear a strange noise...can you detect which direction
                            the sounds are coming from? This ability is important for human beings, but there exist
                            individual differences. Some people are good at telling the source direction of sounds,
                            while others find it difficult. Therefore, we aimed to build a game in which people are
                            allowed to try if they are capable of locating the source of sounds.
                        </p>
