                        </p>
                        <br/>
                        <p>Well let’s take out the two most important words to better understand the meaning of this question.
                            <strong>Virtual </strong> and <strong>Experience.</strong>
                        </p>
                        <br/>
                        <strong>Virtual</strong>
                        <p>
                            <i>Something that is virtual can be done or seen using a computer and therefore without going anywhere or talking to anyone.</i>
                        </p>
                        <br/>
                        <p>You can trace virtual or virtual reality back to the hype of the virtual reality headset, something that has been
                            a hype for some time but this isn’t really the case anymore. <a href="">Enconsultancy, Wired, Forbes</a>
                            <br/><br/>
                            It doesn’t really work because there is an important human factor element in VR. The fact is, if we do not get
                            the human element correct, then no amount of technology will make VR anything more than an interesting tool.
                            Even when VR principles are fully understood, the first implementation is rarely novel and almost never ideal due to the complex
                            nature of VR and the countless possibilities that can be created. <a href="">Jason Jerald. PH.D.</a>
                            <br/><br/>
                            We have to accept human perception and behavior the way it is, not the way logic tells us it should be.
                            Engineering will always be essential as it is the core of VR systems that everything else builds upon,
                            but VR itself presents a fascinating interplay of technology and psychology, and we must understand both to do VR well.
                        </p>
                        <br/>
                        <h3>
                            "Provide the audience with a clear sense of status. More so than anything else—immersion, choice, etc.
                        </h3>
                        <p>
                            <i>~Byron Laviolette, Creative Director of The Mission</i>
                        </p>
                        <br/>
                        <strong>Experience</strong>
                        <p>
                            <i>Something that happens to you that affects how you feel</i>
                        </p>
                        <p>
                            Virtual reality is defined to be a computer-generated digital environment that can be experienced and interacted with as if that environment were real.
                            <i><strong>“The ultimate display would, of course, be a room within which the computer can control the existence of matter.
                                    A chair displayed in such a room would be good enough to sit in and a bullet displayed in such a room would be fatal.
                                    With appropriate programming such a display could literally be the Wonderland into which Alice walked.”
                                </strong></i>
                            ~ Ivan Sutherland 1965
                            <br/><br/>
                            Technically minded people tend to dislike the word “experience” as it is less logical and more subjective in nature.
                            But when asked about their favorite tool or game they often convey emotion as they discuss how, for example, the tool responds.
                            They talk about how it makes them feel often without them consciously realizing they are speaking in the language of emotion.
                            Emotion and cognition are tightly coupled, with cognition more often than not justifying decisions that were actually made emotionally.
                            VR must be experiences with both emotion and logic.
                        </p>
                        <br/>
                        <strong>Immersive</strong>
                        <p>
                            <i>Seeming to surround the audience, player, etc. so that they feel completely involved in something</i>
                        </p>
                        <p>
                            Immersive is the third word which is related to the question. Because it’s a good adjective
                            that combines the two previous words (Virtual & Experience) and is where I believe the core of the question lies.
                            <br/><br/>
                            An immersive world is a place in which anyone can get wrapped up. Whatever your background in life,
                            whatever your political or religious views, and whatever you enjoy doing in your spare time.
                            An immersive world will take you in such that you won't want to leave.
                            <br/><br/>
                            A well designed immersive world would be one that had a personal effect on you. It is,
                            you could say, <strong>evocative,</strong> meaning that it produced something vivid, memorable, and profound in you.
                            For any of us. when we have very vivid memories of being in a place we have them because something about
                            the place stays with us and because we were once, in whatever way, immersed in that world.
                            After the fact we could describe it, in fine detail, to any person who asks us. It's a part of us. <a href="">Skott Lukas</a>
                        </p>
                        <br/>
                        <h3>
                            “If the player's self isn't imported into the world, it's all paper thin.
                            They have to project themselves into it in some way. They need to hear their breath in order for it to be alive."
                        </h3>
                        <p>
                            <i>~Christy Dena, writer-designer-director at Universe Creation 101</i>
                        </p>
                        <br/>
                        <p>
                            <i>This image is what you can call evocative, One of the earliest and more important
                                immersive worlds can be found. Because they were there 30.000 years ago.</i>
                        </p>
                        <br/>
                        <strong>Goal</strong>
                        <p>
                            In my role as a creative technologist I will to look at: <strong>What kind of technology and
                                design principles are needed for true immersion?</strong> This is will be my design and technology
                            study for AC3-STUDIO in Paris 2019. Were I will do research for the studio as a basis to
                            proof myself and help others to better understand this problem. My main question therefore will be:
                        </p>
                        <p>
                            <strong>How do I create a 'truly immersive experience', whereby AC3 can distinguish
                                itself from other creative studios?</strong>
                        </p>
                        <br/>
                        <p>
                            This question will be divided into sub-questions to better understand the problem
                        </p>
                        <ul>
                            <li>What are the preconditions for users to experience immersion?</li>
                            <li>What are the forces and weaknesses of the VR medium?</li>
                            <li>What is the future of immersion? And which techniques are suitable for this?</li>
                            <li>What are the technological possibilities outside VR to get immersion?</li>
                        </ul>
                        <br/>
                        <p>I will use this website as a platform where I’ll post my research and make a log of the progress.</p>
                        <strong>End result</strong>
                        <p>The end result will be an interactive installation where these technologies and design principles will be used as a proof of concept. The Vivid Dream.
                        </p>
