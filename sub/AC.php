                    <h2 class="title">Artificial Creatures</h2>
                    <!--The vivid dream reseach post 1-->
                    <div class="entry">
                        <br/>
                        <p><i>Semester 2 2021</i>
                        </p><br/>
                        <p>For this course, we had to build an Artificial Creature by philosophy-by-doing.
                            The goal was not to focus on technology but on the characteristics of what makes us human.
                            Therefore, we explored various ways to create a small creature named Freddy who is shy about
                            singing in public but sings it longs out when alone. However, it was quite a task to put that
                            much technology in such a small package. </p>
                        <br/>
                        <h3>Freddy the Robot
                        </h3>
                        <br/>
                        <p>Freddy is a friend. This little desk toy is just sitting around, being a great companion when
                            you work, and he quietly observes you all day. But Freddy only really comes to life when no
                            one is watching, have a look at the video:
                        </p>
                        <iframe style="margin: 5px 0 5px 0" width="100%" height="360px"
                                src="https://youtube.com/embed/XoNjuOLNedY">
                        </iframe>
                        <br/>
                        <br/>
                        <p>
                            Freddy is not at all shy. But much like us, he is a different version of himself when he is
                            alone. It was only when I met Freddy that I realized how this is also true for myself:
                            I too sing much more when nobody is listening. I live together with my partner, and although
                            she doesn't mind me singing and I don't mind her listening, I feel much more free to sing and
                            dance when she is not around.
                        </p>

                        <a style ="color: black" href="https://sites.google.com/view/artificial-creatures-2021/portfolios/daniel-simu/freddy" target="_blank">
                            Artificial Creatures website 2021
                        </a>
                        <br/>
                        <br/>
                        <img src="./images/Freddy.gif" style="width:100%" alt="img1">
                        <br/>
                        <br/>
                        <p>
                            <strong>How about you, what do you only do when you're alone?</strong>
                        </p>
                        <p>
                            For humans, identity is always tied to whom we are with. Among every different
                            group of people you will be a different version of yourself. It is not just me
                            saying this, the theory that we have multiple social selves is very well
                            documented. We also have a private self, the identity we take on when in solitude.
                            Freddy was built to remind us of this secret identity we all have, and with a camera hidden
                            in such a way that Freddy couldn't see it we were lucky to record some of his moves and songs.
                            Whenever Freddy starts to sing, he drifts away in a bubble and forgets about
                            the world. But then, when someone walks by, he quickly stops and acts like
                            nothing happened. Sitting on your desk like a limp Toy Story doll, he will
                            patiently wait for you to leave so that he can have his moment to shine.
                        </p>
