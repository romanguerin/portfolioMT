                        <p>This course was in collaboration with the university of munster and the university of Twente
                                about AI, Human Right & Ethics from multiple speakers. We chose to write a paper on the
                            responsible implementation of Artificial Intelligence in Childcare.
                        </p>
                        <br/>
                        <p>
                            <strong>Abstract</strong>
                        </p>
                        <p>
                            Artificial intelligence has increasingly become part of the
                            daily lives of todayâ€™s generation. However, it is still debated how AI
                            applications affect children when they interact with them from an early
                            age. Parents, children and society as a whole benefit when childcare practices lead to healthy outcomes later in life. However, for many (possible)
                            AI applications in childcare it is unclear if their effects promote positive
                            outcomes for children or not. Therefore, the authors argue that there
                            should be some responsibility when deploying AI in childcare, to ensure
                            the effects of AI on the development of children are overwhelmingly positive. AI applications should be safe, accepted, trusted, and closely aligned
                            with human values. To this end, the authors give a broad overview of the
                            current home, education, and healthcare applications while keeping the
                            social and emotional effects in mind. To analyze if these AI applications
                            are designed and implemented in a responsible way, the guidelines of
                            Floridi et al. were used to give an idea of where improvements in current
                            and future applications can be made. This assessment does not cover
                            all human rights and ethical perspectives on AI. Nevertheless, it focuses
                            on the responsibility of AI application and how guardians, teachers, and
                            medical practitioners should incorporate it into childcare.
                        </p>
