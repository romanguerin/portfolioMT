                        <h3>Paper</h3>
                        <br/>
                        <p>
                            <strong>Abstract</strong><br/>
                            Biofeedback is a method of improving one’s ability to control body functions. The
                            following paper intends to develop insight into how auditory feedback of heart rate in an
                            affective game affects the player’s ability to control it. In order to research the matter, a
                            bespoke game has been developed where the player, connected to the set of sensors,
                            controls the game's difficulty by adjusting their heart rate.
                            We gathered data from a series of experiments. First, we recorded participants' in-game
                            performance for both affective and control versions of the game. Unfortunately, the
                            results did not differ between the two groups. However, the paper intends to explain
                            encountered problems and drawbacks of the applied method and include
                            recommendations on how we could improve future studies.
                        </p>
